static int r = 0, w = 0;
void CAGAIN_MFCDlg::OnBnClickedButton2()
{
	// TODO: 在此添加控件通知处理程序代码
	UpdateData();
	R = 0; W = 0;
	int result = equ.get();
	if (answer == result)
	{
		tip = "答对了"; r++;
	}
	else
	{
		tip.Format(_T("答错了，正确答案是：%d"), result); w++;
	} 
	R = r;
	W=w;
	UpdateData(FALSE);
}